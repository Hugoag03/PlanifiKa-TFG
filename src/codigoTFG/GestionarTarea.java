    private void BotonEliminarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarEmpleadoActionPerformed
        // TODO add your handling code here:
        Connection cn = conexion.Conexion.conectar();
        String[] partes = ComboBoxEmpleadoAsignado.getSelectedItem().toString().split(" ", -1);
        String nombre = partes[0];
        String apellidos = partes[1];
        try {
            PreparedStatement ps = cn.prepareStatement("SELECT ID FROM usuarios WHERE Nombre = ? AND Apellidos = ?");

            ps.setString(1, nombre);
            ps.setString(2, apellidos);

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                idEmpleado = rs.getString(1);
                idEmpleadosAsignados.add(rs.getString(1));
            }

            JOptionPane.showMessageDialog(null, "El empleado " + ComboBoxEmpleadoDisponible.getSelectedItem().toString() + " ha sido eliminado de esta tarea");
            int numeroActualEmpleados = Integer.parseInt(CampoEmpleados.getText());
            CampoEmpleados.setText(String.valueOf(numeroActualEmpleados - 1));

            ComboBoxEmpleadoAsignado.removeItemAt(ComboBoxEmpleadoAsignado.getSelectedIndex());

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_BotonEliminarEmpleadoActionPerformed

