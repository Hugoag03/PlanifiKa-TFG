    private void BotonModificarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonModificarTareaActionPerformed
        // TODO add your handling code here:

        Connection cn = conexion.Conexion.conectar();
        String empleados = String.join(",", empleadosAsignados);

        try {

            PreparedStatement ps = cn.prepareStatement("UPDATE tareas SET Nombre = ?, EmpleadosAsignados = ?, FechaEntrega = ?, Estado = ?, Prioridad = ?, Notas = ?, URLArchivoAdjunto = ? WHERE ID = ?");

            ps.setString(1, CampoNombre.getText());
            if (empleados != null) {
                ps.setString(2, empleados);
            }
            ps.setString(3, CampoFecEntrega.getText());
            ps.setString(4, ComboBoxEstado.getSelectedItem().toString());
            ps.setString(5, ComboBoxPrioridad.getSelectedItem().toString());
            ps.setString(6, CampoNotas.getText());
            ps.setString(7, urlImagen);
            ps.setString(8, idTarea);

            ps.executeUpdate();

            if (idEmpleadosNuevos != null) {

                for (String ids : idEmpleadosNuevos) {
                    String[] idsIndividuales = ids.split(",");

                    for (String idEmpleado : idsIndividuales) {
                        if (!idEmpleado.trim().isEmpty()) {

                            PreparedStatement ps2 = cn.prepareStatement("INSERT INTO empleados_Tareas(ID_Tarea, ID_Empleado) VALUES (?, ?)");
                            ps2.setString(1, idTarea);
                            ps2.setString(2, idEmpleado.trim());
                            ps2.executeUpdate();
                        }
                    }
                }
            }

            if (idEmpleadosAsignados != null) {

                for (String ids : idEmpleadosAsignados) {
                    String[] idsIndividuales = ids.split(",");

                    for (String idEmpleado : idsIndividuales) {
                        if (!idEmpleado.trim().isEmpty()) {

                            PreparedStatement ps2 = cn.prepareStatement("DELETE FROM empleados_Tareas WHERE ID_Empleado = ?");
                            ps2.setString(1, idEmpleado.trim());

                            ps2.executeUpdate();
                        }
                    }
                }
            }

            JOptionPane.showMessageDialog(null, "Tarea actualizada correctamente");

            ConsultarTareas consultarT = new ConsultarTareas();
            consultarT.setVisible(true);
            this.dispose();

        } catch (SQLException e) {
            e.printStackTrace();

        }
    }//GEN-LAST:event_BotonModificarTareaActionPerformed

