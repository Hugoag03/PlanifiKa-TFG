    private void BotonCrearTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearTareaActionPerformed
        // TODO add your handling code here:

        if (CampoNombre.getText() != null && CampoFecEntrega != null && CampoNotas.getText() != null) {

            Connection cn = conexion.Conexion.conectar();
            String empleados = String.join(",", empleadosAsignados);

            try {

                PreparedStatement ps = cn.prepareStatement("INSERT INTO tareas (Nombre, EmpleadosAsignados, FechaEntrega, Estado, Prioridad, Notas, URLArchivoAdjunto) VALUES (?, ?, ?, ?, ?, ?, ?)", Statement.RETURN_GENERATED_KEYS);

                ps.setString(1, CampoNombre.getText());
                if (empleados != null) {
                    ps.setString(2, empleados);
                }
                ps.setString(3, CampoFecEntrega.getText());
                ps.setString(4, ComboBoxEstado.getSelectedItem().toString());
                ps.setString(5, ComboBoxPrioridad.getSelectedItem().toString());
                ps.setString(6, CampoNotas.getText());
                ps.setString(7, urlImagen);

                ps.executeUpdate();

                ResultSet rs = ps.getGeneratedKeys();

                if (rs.next()) {
                    idTarea = rs.getString(1);
                    AlmacenarTareas.idsTareas.add(idTarea);
                }

                if (idEmpleadosAsignados != null) {

                    for (String ids : idEmpleadosAsignados) {
                        String[] idsIndividuales = ids.split(",");

                        for (String idEmpleado : idsIndividuales) {
                            if (!idEmpleado.trim().isEmpty()) {

                                PreparedStatement ps2 = cn.prepareStatement("INSERT INTO empleados_Tareas(ID_Tarea, ID_Empleado) VALUES (?, ?)");
                                ps2.setString(1, idTarea);
                                ps2.setString(2, idEmpleado.trim());
                                ps2.executeUpdate();
                            }
                        }
                    }
                }

                JOptionPane.showMessageDialog(null, "Tarea agregada correctamente");

                if (GestionarProyecto.bandera == false) {
                    int numeroActualDeTareas = Integer.parseInt(CrearProyecto.LabelNumeroTareas.getText());
                    CrearProyecto.LabelNumeroTareas.setText(String.valueOf(numeroActualDeTareas + 1));
                }

                if (GestionarProyecto.bandera == true) {
                    int numeroActualEmpleados = Integer.parseInt(GestionarProyecto.LabelNumeroTareas.getText());
                    GestionarProyecto.LabelNumeroTareas.setText(String.valueOf(numeroActualEmpleados + 1));
                }

                int respuesta = JOptionPane.showOptionDialog(null, "Â¿Quiere agregar otra tarea al proyecto?", "Seleccione una opciÃ³n", JOptionPane.YES_NO_OPTION, JOptionPane.YES_NO_OPTION, null, null, null);

                if (respuesta == 0) {

                    CrearTarea crearT = new CrearTarea();
                    crearT.setVisible(true);
                    this.dispose();
                } else {
                    this.dispose();
                }

            } catch (SQLException e) {
                e.printStackTrace();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes rellenar todos los campos");
        }
    }//GEN-LAST:event_BotonCrearTareaActionPerformed

