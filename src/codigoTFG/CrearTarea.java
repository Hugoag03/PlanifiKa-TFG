    private void BotonAgregarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarEmpleadoActionPerformed
        // TODO add your handling code here:
        Connection cn = conexion.Conexion.conectar();

        String[] partes = ComboBoxEmpleadoAsignado.getSelectedItem().toString().split(" ", -1);
        String nombre = partes[0];
        String apellidos = partes[1];
        try {
            PreparedStatement ps2 = cn.prepareStatement("SELECT ID FROM usuarios WHERE Nombre = ? AND Apellidos = ?");

            ps2.setString(1, nombre);
            ps2.setString(2, apellidos);

            ResultSet rs = ps2.executeQuery();
            if (rs.next()) {
                idEmpleadosAsignados.add(rs.getString(1));
            }

            JOptionPane.showMessageDialog(null, "El empleado " + ComboBoxEmpleadoAsignado.getSelectedItem().toString() + " ha sido agregado a esta tarea");
            int numeroActualEmpleados = Integer.parseInt(CampoEmpleados.getText());
            CampoEmpleados.setText(String.valueOf(numeroActualEmpleados + 1));

        } catch (SQLException e) {
            e.printStackTrace();
        }

        empleadosAsignados.add(ComboBoxEmpleadoAsignado.getSelectedItem().toString());
        ComboBoxEmpleadoAsignado.removeItemAt(ComboBoxEmpleadoAsignado.getSelectedIndex());
    }//GEN-LAST:event_BotonAgregarEmpleadoActionPerformed

